char* PetToAsc( char* petscii ) {
// Convert a PETSCII string to an ASCII string

    unsigned char x;

    for(x=0;x<strlen(petscii);x++) {
        linebuffer2[x] = petToAscTable[petscii[x]];
    }
    linebuffer2[x]=0;
    return linebuffer2;
}

char* AscToPet( char* ascii ) {
// Convert a PETSCII string to an ASCII string

    unsigned char x;

    for(x=0;x<strlen(ascii);x++) {
        linebuffer2[x] = ascToPetTable[ascii[x]];
    }
    linebuffer2[x]=0;
    return linebuffer2;
}